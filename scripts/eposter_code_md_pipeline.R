sample_expression_matched <- function(target_genes, n_sets = 100) {
  target_bins <- expr_df$expr_bin[match(target_genes, expr_df$gene)]
  target_bins <- target_bins[!is.na(target_bins)]
  random_sets <- vector("list", n_sets)

  for (i in seq_len(n_sets)) {
    sampled <- unlist(lapply(target_bins, function(bin) {
      pool <- expr_df |>
        dplyr::filter(expr_bin == bin, !(gene %in% marker_genes)) |>
        dplyr::pull(gene)
      if (length(pool) < 1) {
        stop("No expression-matched random gene pool available for expression bin: ", bin)
      }
      sample(pool, size = 1)
    }))
    random_sets[[i]] <- sampled
  }
  random_sets
}

real_module <- intersect(
  c("SOX2", "MKI67", "TOP2A", "LMX1A", "FOXA2", "EN1", "NEUROG2", "DCX", "PITX3", "TH", "DDC"),
  rownames(obj_traj)
)
random_sets <- sample_expression_matched(real_module, n_sets = 100)

random_score_matrix <- sapply(seq_along(random_sets), function(i) {
  genes <- random_sets[[i]]
  Matrix::colMeans(expr_mat[genes, , drop = FALSE])
})

