find_one <- function(pattern) {
  hits <- list.files(raw_dir, pattern = pattern, full.names = TRUE)
  if (length(hits) != 1) {
    stop("Expected exactly one file for pattern: ", pattern, "\nFound: ", paste(hits, collapse = "\n"))
  }
  hits[1]
}

read_geo_10x <- function(gsm, day) {
  prefix <- paste0(gsm, "_", day)
  mtx_file <- find_one(paste0("^", prefix, ".*matrix\\.mtx\\.gz$"))
  barcode_file <- find_one(paste0("^", prefix, ".*barcodes\\.tsv\\.gz$"))
  gene_hits <- list.files(raw_dir, pattern = paste0("^", prefix, ".*(genes|features)\\.tsv\\.gz$"), full.names = TRUE)
  if (length(gene_hits) != 1) {
    stop("Expected exactly one genes/features file for prefix: ", prefix)
  }

  mat <- ReadMtx(
    mtx = mtx_file,
    cells = barcode_file,
    features = gene_hits[1],
    feature.column = 2
  )

  obj <- CreateSeuratObject(counts = mat, project = prefix, min.cells = 0, min.features = 0)
  obj$gsm <- gsm
  obj$day <- day
  obj$stage <- samples$stage[match(gsm, samples$gsm)]
  obj$source_scope <- "D8-D35_in_vitro_differentiation"
  obj
}

